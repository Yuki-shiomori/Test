package jp.co.aforce.sample;

public class Relational {
	public static void main(String[] args) {
		int a = 11, b = 3;
		System.out.println(" a > b= " + ( a > b));
		System.out.println(" a < b= " + ( a < b));
		System.out.println(" a >= b= " + ( a >= b));
		System.out.println(" a <= b= " + ( a <= b));
		System.out.println(" a == b= " + ( a == b));
		System.out.println(" a != b= " + ( a != b));
	}
}
